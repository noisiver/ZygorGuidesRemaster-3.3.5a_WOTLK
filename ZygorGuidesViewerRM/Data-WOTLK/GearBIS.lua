overlay_curated_data(BISData, curated)

local coverage = {}
for classToken, classData in pairs(BISData) do
	coverage[classToken] = {}
	for buildNum, buildData in pairs(classData) do
		local slotCounts = {}
		local itemCounts = {}
		for _, category in ipairs(BIS_CATEGORY_KEYS) do
			local slots = 0
			local items = 0
			for _, itemIDs in pairs(buildData[category] or {}) do
				slots = slots + 1
				items = items + #itemIDs
			end
			slotCounts[category] = slots
			itemCounts[category] = items
		end
		coverage[classToken][buildNum] = {
			slots = slotCounts,
			items = itemCounts,
		}
	end
end

ItemScore.BISCategoryKeys = BIS_CATEGORY_KEYS
ItemScore.BISData = BISData
ItemScore.BISCoverage = coverage
